def generate_agent(agent_info, category, output_dir):
    """生成单个Agent的完整文档"""
    agent_dir = output_dir / f"{agent_info['id']}_{agent_info['name']}"
    agent_dir.mkdir(parents=True, exist_ok=True)
    
    # 生成5个标准文档
    (agent_dir / "SKILL.md").write_text(
        generate_skill_md(agent_info, category), encoding='utf-8'
    )
    (agent_dir / "requirement.md").write_text(
        generate_requirement_md(agent_info), encoding='utf-8'
    )
    (agent_dir / "design.md").write_text(
        generate_design_md(agent_info, category), encoding='utf-8'
    )
    (agent_dir / "tasks.md").write_text(
        generate_tasks_md(agent_info), encoding='utf-8'
    )
    (agent_dir / "checklist.md").write_text(
        generate_checklist_md(agent_info), encoding='utf-8'
    )
    
    return agent_dir


